#include <iostream>
#include <iomanip>
 

using namespace std;
 

int main()
{
 
    
    cout << static_cast<char>(toupper('#')) << endl;
    cout << static_cast<char>(toupper('k')) << endl;
    cout << static_cast<char>(toupper('3')) << endl;
    cout << static_cast<char>(toupper('-')) << endl;
    cout << static_cast<char>(tolower('T')) << endl;
    cout << static_cast<char>(tolower(':')) << endl;
    cout << static_cast<char>(tolower('u')) << endl;
    cout << static_cast<char>(tolower('{')) << endl;
 
    
 
 
    return 0;
}